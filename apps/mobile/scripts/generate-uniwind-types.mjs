// Writes apps/mobile/uniwind-types.d.ts with the same content uniwind's metro
// transformer writes when `expo start` runs. The d.ts is gitignored because
// uniwind regenerates it on every metro start, but typecheck (and CI) needs
// it to exist before tsc runs — so we materialise it here.
//
// The themes list must match `withUniwindConfig` defaults in metro.config.js
// (light + dark, plus any extraThemes).
import { writeFileSync } from "node:fs"
import { fileURLToPath } from "node:url"
import { dirname, join } from "node:path"

const themes = ["light", "dark"]

const stringified = `[${themes.map((t) => `'${t}'`).join(", ")}]`

const content = `// NOTE: This file is generated by uniwind and it should not be edited manually.
/// <reference types="uniwind/types" />

declare module 'uniwind' {
    export interface UniwindConfig {
        themes: readonly ${stringified}
    }
}

export {}
`

const out = join(dirname(fileURLToPath(import.meta.url)), "..", "uniwind-types.d.ts")
writeFileSync(out, content)
